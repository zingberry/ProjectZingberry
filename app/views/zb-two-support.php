        	<h2>Support</h2>
            <br />
            <p>Zingberry allows you to stay connected with your Rutgers University campus. Meet and search for new people who share similar interests with you from your classes, dorms, organizations and more. Video Chat request users you think are cool. Or just instant message them. All with Zingberry. <p>
<br />
<br />
<b>1.	How do I start using Zingberry? </b><br />
<b>A.</b> Please register using your Rutgers Eden or Scarlet Mail email address. At this time, Zingberry is only open to Rutgers University New Brunswick  students. Provide your full name, make up a password, and click register. You should soon receive a verification email. As soon as your email address is verified, you are ready to use the site. 
<br />
<br />
<b>2.	How do I Login?<br />
A.</b>	Login by clicking the login tab on the upper left side on the home page. Enter your Rutgers Email and Password and click Login.
<br />
<br />
<b>3.	What if I forgot my password?<br />
A.</b>	If you happen to forget your password, click on the forgot password hyperlink in the login tab that will ask you for your Rutgers email address. A reset password email should be sent to the email you registered with.
<br />
<br />
<b>4.	What if I require help after reading the entire tutorial? <br />
A.</b>	If you still require help after reading the support page, please email us using your Rutgers email at <a href="mailto:support@zingberry.com">support@zingberry.com</a>   
<br />
<br />
<p align="center"><b> Zingberry Tiles </b></p>
<b>5.	What is Zingberry Tiles? <br />
A.</b>	 Zingberry Tiles is where you will be able to search, discover and connect with people on your entire campus. Your screen will display the pictures of Rutgers Students based on search results. You will be able to view a user's information and send a video chat request through a drop down menu.
<br />
<br />
<b>6.	How do I view a user's information?<br />
A.</b>	 Simply move your mouse to hover over a user's image. Four categories listing user information appear: Personal Information, Academics, Organizations, and Interests. This is where you can learn more about a user based on what information they make public.
<br />
<br />
<b>7.	How do I search for users with an interest or similarity?<br />
A.</b>	If you decide you want to discover people with a similarity (ex: visual arts majors), you can  search by typing it in the search bar located in the header, selecting the category to search in the drop down menu(ex: major), and hitting the enter key. People who fit the search results will be displayed as a tile.
<br />
<b>8.	How do I video chat request a user?<br />
A.</b>	If you decide to request a video chat with a user, click send chat request in the drop down menu. You will be prompted to send an optional message. The user will receive a notification in their chat requests tab that you have requested a video chat.  
<br />
<br />
<b>9.	 How do I view people who video chat requested me?<br />
A.</b>	You must be on the video chat page (click the video camera icon in the header). Click the chat request tab in the lower left section of the screen. You will see a list of people who have requested a video chat with you. If you click on a name, you will be redirected to a page where you can see everyone who has requested you in a tile view format. 
<br />
<br />
<b>10.	 How do I accept a video chat request?<br />
A.</b>	When you are on the chat requests tab, hover over a user's name to access the drop down menu. Then click Accept Request. A real time message should be sent to the user asking to initiate a video chat conference. As soon as they accept, you will be video chatting!
<br />
<br />
<b>11.	How do I reject a video chat request?<br />
A.</b>	Click the X icon next to a user's name. 
<br />
<br />
<p align="center"><b>Zingberry Accounts </b></p>
<br />
<br />
<b>12.	How do I go to Accounts?<br />
A.</b>	When you are on the Zingberry Tiles, go to the header and click the human icon on the upper right side of the page (next to the video camera icon).
<br />
<br />
<b>13.	How do I add  and edit information?<br />
A.</b>	When you are in Accounts, start filling out your basic info found in Personal Information. This information will allow other users to find you based on similar interests. 
<br />
<br />
<b>14.	How do I enter information about my accounts, organizations, and interests?<br />
A.</b>	Use the accounts settings tab on the upper left side of the screen. Continue the same way as from the Personal Information section. 
<br />
<br />
<b>15.	How do I upload my picture?<br />
A.</b>	Click on the accounts settings tab on the upper left side of the screen. Select a file from your computer by clicking the choose file button. Once a picture is selected, click upload. You will be able to crop your user image by dragging your mouse over the uploaded image.




